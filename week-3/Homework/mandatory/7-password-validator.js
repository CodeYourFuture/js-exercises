function validPassword(password, index, arr) {
  let pwd = /^(?=.*[\d])(?=.*[A-Z])(?=.*[a-z])(?=.*[!@#$%^&*])[\w!@#$%^&*]{5,}$/;
  let tst = pwd.test(password);
  let prePwd = arr.indexOf(password) === index;
  return ( tst && prePwd);
}

function validatePasswords(passwords){
  return passwords.map(validPassword);
}

